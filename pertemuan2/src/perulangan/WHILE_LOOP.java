public class WHILE_LOOP {
    // PENGOPRASIAN PERULANGAN
   public static void main(String[] args) {
        int ANGKA = 1;

        while (ANGKA <= 5) {
            System.out.println("Nilai : " + ANGKA);
            ANGKA++;
        }
    }
}